		<p><span style="font-size: medium;">Kebijakan Privasi</span></p>
<p><strong>Ruang Lingkup</strong></p>
<p style="text-align: justify;">Divisi HSSE PT PGN mengembangkan HSSE Project Assurance Service System (HSSE PASS), merupakan passport bagi para pekerja konstruksi yang digunakan di lingkungan proyek PGN secara online dan Aplikasi Mobile, yang deselengarakan penuh oleh Divisi HSSE PT PGN, Tbk. Kebijakan Privasi ini ditetapkan untuk melindungi informasi yang Anda berikan ketika Anda masuk dan menggunakan aplikasi kami. Data yang kami simpan hanya akan digunakan untuk mengidentifikasi ketika Anda kembali menggunakan aplikasi ini. Kami berkomitmen untuk menjaga kepercayaan Anda dengan memastikan privasi Anda terlindungi.</p>
<p style="text-align: justify;">Kebijakan Privasi ini dapat berubah sewaktu-waktu tanpa pemberitahuan terlebih dahulu. Perubahan terhadap kebijakan ini akan diinfokan melalui situs https://hssepass.pgn.co.id atau sarana lainnya. Anda diharapkan untuk melakukan pengecekan secara berkala pada halaman ini, agar dapat mengetahui jika ada perubahan. Dengan tetap mengakses dan menggunakan aplikasi ini, Anda dianggap telah menyetujui perubahan-perubahan ketentuan pada Kebijakan Privasi ini.</p>
<p><strong>Registrasi</strong></p>
<p style="text-align: justify;">Semua orang dapat mengakses, melihat Hasil test pekerja dan absensi yang telah diregistrasikan melalui sistem internal PGN. </p>
<p><strong>Informasi yang perlukan</strong></p>
<p>Informasi lengkap atas identitas diri Anda yang kami minta melalui aplikasi ini adalah:</p>
<ol>
<li>Nama Lengkap&nbsp;</li>
<li>Alamat Surat Elektronik (email)&nbsp;</li>
<li>Nomor Telepon Genggam&nbsp;</li>
<li>Alamat Lengkap&nbsp;</li>
<li>Alamat Pengiriman&nbsp;</li>
</ol>

<p><strong>Mengapa kami memerlukan informasi Anda?</strong></p>
<p>Kami memerlukan informasi Anda, untuk dipergunakan dalam:</p>
<ul>
<li>Melihat data profil pekerja.</li>
<li>Mencegah potensi aktivitas yang dilarang atau ilegal, dan menggunakan Perjanjian Pengguna Kami;</li>
<li>Mengubah,mengukur dan meningkatkan kinerja sistem Kami;</li>
<li>Membandingkan informasi untuk keakuratan, dan memverifikasi dengan pihak ketiga;</li>
<li>Menghubungi Anda jika ada kepentingan – kepentingan lain yang berkaitan dengan survey atas pengembangan dan juga peningkatan kinerja perkerja konstruksi</li>
<li>Menginformasikan secara berkala kepada Anda melalui email blast/sms blast mengenai aktivitas/promo yang sedang berlangsung, berdasarkan pilihan komunikasi anda; dan Kelengkapan administrasi kami.</li>
</ul>
<p><strong>Keamanan dan Hukum yang Berlaku</strong></p>
<p style="text-align: justify;">Kami menjamin keamanan data yang Anda berikan kepada kami dan kami tidak akan menjual, mendistribusikan, atau menyewakan informasi pribadi Anda kepada pihak ketiga, kecuali jika kami diharuskan oleh undang-undang yang berlaku di Indonesia untuk melakukannya. Kami dapat mengkombinasikan informasi Anda dengan informasi yang Kami kumpulkan dari perusahaan lainnya dan menggunakannya untuk meningkatkan dan mengubah layanan, konten dan iklan Kami.</p>
<p>&nbsp;</p>
<p>Kami membagi informasi pribadi Anda dengan:</p>
<ul>
<li>Para penyedia jasa yang dikontrak untuk membantu operasi bisnis Kami (seperti perusahaan pengiriman, penyedia jasa pembayaran internet, perusahaan peneliti pasar, perusahaan pengembangan perangkat lunak, investigasi penipuan, bill collection, joint programs and awards dan co-branded credit cards).</li>
<li>Penegak hukum atau pejabat pemerintah lainnya, sebagai tanggapan untuk memverifikasi permintaan terkait investigasi kriminal atau dugaan aktivitas ilegal. Dalam kasus seperti itu, Kami akan memberikan informasi yang relevan untuk investigasi, seperti nama, kota, kode pos, nomor telepon, alamat email, informasi akun, riwayat akun pengguna, alamat IP, keluhan penipuan, dan riwayat katalog.</li>
</ul>
<p>Syarat-syarat dan ketentuan dalam Kebijakan Privasi ini tunduk kepada hukum di wilayah Negara Kesatuan Republik Indonesia.</p>
<p><strong>Cookies</strong></p>
<p style="text-align: justify;">Kami menggunakan cookies (file kecil di dalam hard drive Anda) pada halaman tertentu untuk membantu Kami menganalisis aliran halaman web Kami, mengubah layanan Kami, konten dan iklan, mengukur keefektifan promosi, dan mempromosikan kepercayaan dan keamanan. Untuk informasi lebih lanjut mengenai kenapa Kami menggunakan cookies, detailnya bisa dilihat di Perjanjian Pengguna dengan konten cookies.</p>
<p>Sedikit hal penting yang harus Anda tahu tentang cookies adalah:</p>
<ul>
<li>Kami menawarkan fitur tertentu yang hanya tersedia melalui penggunaan cookies.</li>
<li>Kami gunakan cookies untuk membantu mengenali Anda dan menjaga status sign in Anda.</li>
<li>Kebanyakan cookies adalah “cookies sesi,” artinya mereka terhapus secara otomatis dari hard drive Anda saat sesi berakhir.</li>
<li>Anda selalu bebas menolak cookies Kami jika browser Anda membolehkan, walaupun dengan melakukan itu dapat mengganggu Anda saat menggunakan situs atau layanan Kami.</li>
</ul>
<p><strong>Mengakses, Meninjau dan Mengubah Informasi Pribadi Anda</strong></p>
<p style="text-align: justify;">Anda bisa melihat, meninjau dan mengubah banyak informasi pribadi Anda dengan masuk ke Situs Kami. Umumnya, Kami tidak akan mengubah secara manual informasi pribadi Anda karena sangat sulit memverifikasi identitas Anda dari jauh. Anda harus segera memperbarui informasi pribadi Anda jika berubah atau tidak akurat. Begitu Anda posting, Anda tidak bisa mengubah atau menghapusnya. Atas permintaan Anda ke customer service, Kami akan menutup akun Anda dan menghapus informasi pribadi Anda sesegera mungkin, berdasarkan aktivitas akun Anda dan sesuai dengan hukum yang berlaku. Kami akan mempertahankan informasi pribadi Anda dari akun yang ditutup untuk mengikuti hukum, mencegah penipuan, mengambil hutang, menyelesaikan perselisihan,mengatasi masalah, membantu melakukan investigasi, menjalankan Ketentuan Penggunaan Situs Kami, dan mengambil tindakan lain sesuai hukum.</p>
<p><strong>Keamanan</strong></p>
<p style="text-align: justify;">Informasi Anda disimpan dalam server Kami di Indonesia. Kami memperlakukan data sebagai aset yang harus dilindungi dan menggunakan banyak alat (enkripsi, password, keamanan fisik, dsb.) untuk melindungi informasi pribadi melawan akses yang tidak diizinkan dan pembeberan. Namun, seperti yang mungkin Anda tahu, pihak ketiga dapat menahan secara hukum atau mengakses transmisi atau komunikasi pribadi, dan pengguna lain dapat menyalahgunakan informasi pribadi Anda yang mereka kumpulkan dari situs. Maka dari itu, meskipun Kami sangat bekerja keras melindungi privasi Anda, Kami tidak dapat menjanjikan, dan akan tetap terdapat celah bahwa informasi pribadi atau komunikasi pribadi Anda tidak selalu tetap pribadi.</p>
<p style="text-align: justify;">Kami menjaga privasi pelanggan kami dengan serius dan kami hanya akan mengumpulkan, merekam, menahan, menyimpan, mengungkapkan, memindahkan dan menggunakan informasi pribadi Anda seperti diuraikan di atas.</p>

